				    <h2>Posted: 12/10/2014<br>By: Pugvillage</h2>
					<h3>Get a Pug</h3>
					
					<img src="../images/thoughtful.jpg" alt="Pug pondering life's wonders">
					<p>
					Pugs are a wonderful breed of dog, however they’re not for 
					everyone. As Pug advocates it’s our responsibility to 
					provide the negative aspects of Pug ownership. We take this 
					approach because we want Pugs to be in appropriate homes, 
					for their benefit as well as your own.
                    
                    When selecting a dog, it’s vitally important to match breed 
                    with owner, so that the experience for all involved is a 
                    positive one. There are many things you should consider 
                    before you even begin your search, and what follows is a 
                    compilation of the most commonly mentioned downsides to 
                    Pugs. This article is designed to focus on the people side 
                    of Pug ownership, to help you decide whether your 
                    personality and lifestyle fits with the nature and 
                    characteristics of the Pug breed. We urge you to consider 
                    these downsides carefully and seriously before deciding on 
                    buying a Pug:</p>
                    
                    <h3>Health Issues:</h3><p>
                    The bottom line regarding Pugs and health is that Pugs are 
                    prone to a myriad of genetic health issues, and require more
                    veterinary care than the average breed of dog. If you get a 
                    Pug, be prepared to make a lot of trips to the vet. Not 
                    every Pug will require frequent vet visits, but many do, so 
                    it’s in your best interest to plan on spending a lot of 
                    time, and money at the vets office. If you don’t have the 
                    time, money or willingness to commit the next 12 years to a 
                    dog that may have frequent and significant health problems, 
                    don’t get a Pug.</p>
                    
                    <h3>Shedding:</h3><p>
                    As mentioned in our Pug FAQ’s section, Pugs shed a lot. In 
                    fact, they shed more than a lot. They shed tons. If you read
                    or hear anything to the contrary, you’re either getting 
                    misinformation, or the input of someone whose Pug is a rare 
                    exception to the norm. If you get a Pug, you’ll have fur 
                    all over the place. On every piece of furniture, on all your
                    clothes and in your car. You don’t even have to put your Pug
                    in the car, the fur will just be there…and everywhere else. 
                    If this is at all a concern to you, don’t get a Pug.</p>
                    
                    <h3>Housetraining:</h3><p>Pugs are not the easiest dogs in the
                    world to housetrain. They’re small, which makes them 
                    inherently more difficult to housetrain than large dogs, 
                    which have a greater capacity to “hold”. Their size may not 
                    be the biggest obstacle to housetraining however, as Pugs 
                    tend to have a stubborn streak which makes them less than 
                    cooperative students. Skilled and experienced dog owners 
                    usually manage to housetrain their Pugs within 3 months of 
                    bringing their dog home. The majority of Pug owners however,
                    often find housetraining a task that takes a year or even 
                    longer. If the idea of a years worth of poops and pee on the 
                    carpet isn’t tolerable to you, don’t get a Pug. </p>
                    
                    <h3>A Pug is Your Shadow:</h3><p>Pugs are clingy dogs, because 
                    they’re people dogs which thrive on human companionship. 
                    This shouldn’t come as any surprise, because they were bred 
                    to be companion dogs. If you get a Pug, expect it to be at 
                    your feet and under your feet all the time. Not once in a 
                    while, or during meal time…all the time. A Pug will follow 
                    you, everywhere. Some people find this endearing, other 
                    people find it maddening or at least occasionally annoying. 
                    Think long and hard about this one, because you may not 
                    realize it bothers you until it happens. If this clingy 
                    nature is something that you think might bother you, don’t 
                    get a Pug.</p>
                    
                    <h3>Pugs Don’t Catch Frisbees:</h3><p>Pugs are low activity 
                    dogs. This means that they sleep a lot, as much as fourteen 
                    hours a day. It also means that Pugs have short bursts of 
                    energy, so you won’t see a Pug run very long or very far 
                    before it slows down and retreats for a nap. A Pug isn’t 
                    going to jog along side you on the sidewalk. It won’t even 
                    consider trying to catch a Frisbee. Most Pugs won’t even 
                    fetch a ball or a stick. If you’re an outdoor person seeking
                    to share your active outdoor lifestyle with a dog, don’t get
                    a Pug.</p>
                    
                    <h3>Pugs are Indoor Dogs:</h3><p>Stated quite simply, Pugs 
                    cannot tolerate high temperatures and humidity. This type 
                    of weather is unhealthy for Pugs, and over exposure to this 
                    type of weather can cause immediate or long-term health 
                    problems ranging from heat stroke to organ damage. If you 
                    live in a warm weather climate, and you don’t have air 
                    conditioning, don’t get a Pug.</p>
                    
                    <h3>Pug Maintenance:</h3><p>Pugs require a fair amount of 
                    grooming and general care. They have to be brushed 
                    frequently due to minimize shedding. Pugs have facial folds 
                    which need to be cleaned, every other day, every week or 
                    every month depending on the dog. Their nails grow fast, 
                    very fast, and need to be trimmed often. Pugs are also prone
                    to having their anal sacs fill, and these sacs in turn, must
                    be drained from time to time…not a pleasant, or easy task if
                    you chose to do it yourself. If you won’t do it, then you’ll
                    need to take your Pug to the vet to have it done, sometimes 
                    several times per year. If you’re looking for a low 
                    maintenance dog which requires minimal grooming, don’t get a
                    Pug.</p>
                    
